public class Bulat01 {
    public static void main(String[] args) {
        // deklarasi variable
        byte b;
        int i;
        short s;
        long l;
        double d;
        char c;
        String S;
        
        // inisialisasi nilai
        b = 30;
        i = 12;
        s = 23;
        l = 11;
        d = 2.3;
        c = 68;
        S = "Ashokani";
        
        // menampilkan nilai
        System.out.println("nilai dari variable byte `b` = "+b);
        System.out.println("nilai dari variable int `i` = "+i);
        System.out.println("nilai dari variable short `s` = "+s);
        System.out.println("nilai dari variable long `l` = "+l);
        System.out.println("nilai dari variable double `d` = "+d);
        System.out.println("nilai dari variable char `c` = "+c);
        System.out.println("nilai dari variable String `S` = "+S);
    }
}
